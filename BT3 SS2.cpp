#include <stdio.h>

int main()
{
    int a = 10;
    int b = 5;
    int sum = a + b;
    int Hieu = a - b;
    int Tich = a * b;
    int Thuong = a / b;
    printf("Tong cua %d và %d là: %d\n", a, b, sum);
    printf("Hieu cua %d và %d là: %d\n", a, b, Hieu);
    printf("Tich cua %d và %d là: %d\n", a, b, Tich);
    printf("Thuong cua %d và %d là: %d\n", a, b, Thuong);
    return 0;
}

