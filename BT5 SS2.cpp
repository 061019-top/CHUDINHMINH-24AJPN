#include <stdio.h>

int main()
{
    int dai = 10;
    int rong = 5;
    int chuvi = 2 * (dai + rong);
    int dientich = dai * rong;
    printf("Chieu dai cua hinh chu nhat la: %d\n", dai);
    printf("Chieu rong cua hinh chu nhat la: %d\n", rong);
    printf("Chu vi cua hinh chu nhat la: %d\n", chuvi);
    printf("Dien tich cua hinh chu nhat la: %d\n", dientich);
    return 0;
}

